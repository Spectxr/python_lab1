# % кода созданного ИИ - 35%

text = input('Введите строку: ')

# Переменная для накопления результата.
result = ''

# Счетчик пробелов подряд.
counter = 0

for char in text:
    # Если встретили пробел, то увеличиваем счетчик на 1.
    if char == ' ':
        counter += 1
    # Если встретили не пробел, то обрабатываем накопленные пробелы, сбрасываем счетчик и добавляем к результату текущий символ.
    else:
        if counter == 1:
            result += ' '
        elif counter >= 2:
            result += '*'
        counter = 0
        result += char

# Обработка случая, когда строка заканчивается пробелами.
if counter == 1:
    result += ' '
if counter >= 2:
    result += '*'

print('Строка после замены пробелов на *:', result)
